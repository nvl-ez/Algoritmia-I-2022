    private void crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearActionPerformed
        if(selectorTipo.getSelectedIndex()==1){
            Any any;
            if(selectorAny.getSelectedIndex()==0){
                any = Any.primero;
            }
            else{
                any = Any.segundo;
            }
            curso = new Batxiller(NombreText.getText(), Integer.parseInt(CodigoText.getText()), any);
        }
        else{
            Especialidad especialidad;
            if(selectorEspecialidad.getSelectedIndex() == 0){
                especialidad = Especialidad.informatica;
            }
            else if(selectorEspecialidad.getSelectedIndex() == 1){
                especialidad = Especialidad.electronica;
            }
            else{
                especialidad = Especialidad.mecanica;
            }
            curso =  new Fp(NombreText.getText(), Integer.parseInt(CodigoText.getText()), especialidad);
        }
        listaCurso.addElemento(curso);
        
        NombreText.setText("");
        CodigoText.setText("");
        
        AltaCursos.setVisible(false);
        crearAsignatura.setVisible(true);
    }//GEN-LAST:event_crearActionPerformed

