    private void buscar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar3ActionPerformed
        String text = "";
        for(int i = 0; i<listaCurso.getLength(); i++){
            ListaAsignatura asigList = listaCurso.getElemento(i).getAsignaturas();
            boolean encontradoCurs = false;
            String asignaturas = "";
            
            for(int j=0; j<asigList.getLength(); j++){
                ListaEstudiante estudList = asigList.getElemento(j).getEstudiantes();
                boolean encontradoAsig = false;
                
                for(int k = 0; k<estudList.getLength() && !encontradoAsig; k++){
                    Estudiante estud = estudList.getElemento(k);
                    if(estud.getNombre().equals(estudainteBuscar.getText()) && estud.getDNI().equals(DNIBuscar.getText())){
                        encontradoAsig = true;
                    }
                }
                if(encontradoAsig){
                    asignaturas = asignaturas+"\t"+asigList.getElemento(j).toString();
                    encontradoCurs = true;
                }
            }
            
            if(encontradoCurs){
                text = text+listaCurso.getElemento(i).toString()+asignaturas+"\n";
            }
        }
        
        if(text.length()>1){
            estudianteTextArea.setText(text);
        }
        else{
            estudianteTextArea.setText("No se han encontrado asignaturas para este estudiante.");
        }
    }//GEN-LAST:event_buscar3ActionPerformed

