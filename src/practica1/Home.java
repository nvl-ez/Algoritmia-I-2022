    private void buscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar1ActionPerformed
        int i = 0;
        String text;
        ListaEstudiante estList = new ListaEstudiante();
        boolean encontrado = false;
        for(; i< listaCurso.getLength() && !encontrado; i++){
            ListaAsignatura asigList = listaCurso.getElemento(i).getAsignaturas();
            for(int j =0; j<asigList.getLength() && !encontrado; j++){
                Asignatura asig = asigList.getElemento(j);
                if(asig.getNombre().equals(nombreBuscar.getText())){
                    if(asig.getCodigo() ==Integer.parseInt(codigoBuscar.getText())){
                        if(asig instanceof Obligatoria && tipoBuscar.getSelectedIndex()==2){
                            if(Integer.parseInt(creditosBuscar.getText())== ((Obligatoria)asig).getCreditos()){
                                encontrado = true;
                                estList = asig.getEstudiantes();
                            }
                        }
                        else if(asig instanceof Optativa && tipoBuscar.getSelectedIndex()==1){
                            if(perfilBuscar.getSelectedIndex() ==0 && ((Optativa)asig).getTipo()==Tipo.teorico){
                                encontrado = true;
                                estList = asig.getEstudiantes();
                            }
                            else if(perfilBuscar.getSelectedIndex() ==1 && ((Optativa)asig).getTipo()==Tipo.practico){
                                encontrado = true;
                                estList = asig.getEstudiantes();
                            }
                        }
                    }
                }
            }
        }
        if(encontrado){
            text = listaCurso.getElemento(i-1).toString();
            if(estList.getLength()>0){
                text=text+"\nEstudiantes:\n"+estList.toString();
            }
            else{
                text=text+"\n No hay estudiantes en esta asignatura";
            }
        }
        else{
            text = "No existe la asignatura especificada";
        }
        buscarTextArea.setText(text);
    }//GEN-LAST:event_buscar1ActionPerformed

