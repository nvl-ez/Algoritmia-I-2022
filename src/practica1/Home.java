    private void anadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_anadirActionPerformed
        Asignatura asignatura;
        Tipo perfil;
        if(tipoAsignatura.getSelectedIndex() == 1){
            if(selectorPerfil.getSelectedIndex() == 0){
                perfil = Tipo.practico;
            }
            else{
                perfil = Tipo.teorico;
            }
            asignatura = new Optativa(nombreAsignatura.getText(), Integer.parseInt(codigoAsignatura.getText()), perfil);
            curso.addAsignatura(asignatura);
        }
        else if(tipoAsignatura.getSelectedIndex() == 2){
            asignatura = new Obligatoria(nombreAsignatura.getText(), Integer.parseInt(codigoAsignatura.getText()), Integer.parseInt(creditos.getText()));
            curso.addAsignatura(asignatura);
        }
        nombreAsignatura.setText("");
        codigoAsignatura.setText("");
        creditos.setText("");
        
    }//GEN-LAST:event_anadirActionPerformed

